    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = ClienteLista.getSelectedRow();

        DefaultTableModel modelo = (DefaultTableModel) ClienteLista.getModel();

        if (linhaSelecionada >= 0) {
            int idSelecionado = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());

            String nome = modelo.getValueAt(linhaSelecionada, 1).toString();

            String email = modelo.getValueAt(linhaSelecionada, 2).toString();

            String cpf = modelo.getValueAt(linhaSelecionada, 3).toString();

            var cliente = new Cliente(nome,cpf, email);

            var telaAlterar = new EditarCliente(cliente);
            telaAlterar.setVisible(true);

            ClienteLista.revalidate();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

